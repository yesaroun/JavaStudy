// 비트 단위 연산자

public class Test025{
	public static void main(String[] args){
		int a = 13, b = 7;
		int c, d, e;

		c = a & b;
		d = a | b;
		e = a ^ b;

		System.out.printf("a & b : %d%n", c);
		System.out.printf("a | b : %d%n", d);
		System.out.printf("a ^ b : %d%n", e);
	}
}

// 실행결과
/*
a & b : 5
a | b : 15
a ^ b : 10
계속하려면 아무 키나 누르십시오 . . .
*/

/*
	13 → 00001101
  &  7 → 00000111  (둘다 1이면 1)
  ----------------
  		  00000101  → 5


	13 → 00001101
  |  7 → 00000111  (하나라도 1이면 1)
  ----------------
  		  00001111  → 15


	13 → 00001101
  ^  7 → 00000111  (서로 다르면 1)
  ----------------
  		  00001010 → 10

*/